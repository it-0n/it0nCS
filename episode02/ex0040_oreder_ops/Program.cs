using System;

class Program
{
    static void Main()
    {
        // Пример 1: Без изменения порядка
        int result1 = 3 + 4 * 5; // Умножение выполняется первым
        WriteLine("Результат 1: 3 + 4 * 5 = " + result1);

        // Пример 2: С изменением порядка
        int result2 = (3 + 4) * 5; // Скобки меняют порядок выполнения
        WriteLine("Результат 2: (3 + 4) * 5 = " + result2);

        // Пример 3: Деление и остаток от деления
        int result3 = 20 / 4 % 3; // Сначала деление, затем остаток
        WriteLine("Результат 3: 20 / 4 % 3 = " + result3);

        // Пример 4: Использование Math.Pow для возведения в степень
        double result4 = Math.Pow(2, 3) + 5; // Возведение в степень первым
        WriteLine("Результат 4: 2^3 + 5 = " + result4);

        // Пример 5: Изменение порядка операций с Math.Pow
        double result5 = Math.Pow(2 + 3, 2); // Скобки меняют порядок
        WriteLine("Результат 5: (2 + 3)^2 = " + result5);
    }
}